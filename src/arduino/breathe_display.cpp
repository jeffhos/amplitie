BreatheDisplay::BreatheDisplay(Strand* strandP)
  : m_strandP(strandP)
{
  m_cycle = 0;
}


void BreatheDisplay::update()
{
  unsigned char color = PATTERN[m_cycle];
  for (int i = 0; i < m_strandP->getLength(); i++) {
    m_strandP->setColor(i, Color(color, color, color));
  }
  m_cycle++;
  if (m_cycle >= 800) {
    m_cycle = 0;
  }
}


long BreatheDisplay::updateTime()
{
  return 10L;
}